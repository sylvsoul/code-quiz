// Logs window object
console.log(window);

// Logs reference to the document in the window object
console.log(window.document);

// Logs root element of document.
console.log(document.documentElement);

// Returns head element of current document
console.log(document.head);

// bonus: log out the length of the window object
// this length property is the number of "window"
// objects that are open in the browser
// zero indicates the first window object (as an array)
console.log(window.length)